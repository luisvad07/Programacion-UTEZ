# Variables para nombre y edad
nombre = input("Ingrese su nombre: ")
edad = input("Ingrese su edad: ")

# Convertir edad a número entero
edad = int(edad)

# Hacer la comparación
if edad > 18:
    print(nombre + ", eres mayor de 18 años.")
elif edad > 0 == edad < 18:
    print(nombre + ", eres menor de 18 años.")
else:
    print("Opción no soportado")

