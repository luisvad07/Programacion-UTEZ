public class Operaciones {

    public int sumarNumeros(int a, int b){
        return a + b;
    }

    public int restarNumeros(int a, int b){
        return a - b;
    }

    public int multiplicarNumeros(int a, int b){
        return a * b;
    }

    public double dividirNumeros(double a, double b){
        return a / b;
    }
}
